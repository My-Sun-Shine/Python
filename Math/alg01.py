# 汉诺塔算法
# 有三根杆子A，B，C
# A杆上有N个(N>1)穿孔圆盘，盘的尺寸由下到上依次变小，
# 要求安装下列规则把所有的圆盘移动到C杆
# 1.每次只能移动一个圆盘
# 2.大盘不能叠在小盘上面
# 3.可将圆盘临时置于B杆上，也可以从A杆移动走的圆盘重新移动回C杆上
# 解题思想：先把A杆上的N-1个圆盘移动到B杆上，再把A上的最后一个大盘移动到C上，最后把B上的N-1个圆盘移动到C上


def move(n, a, b, c):
    if n == 1:
        print(a, "==>", c)
    else:
        move(n-1, a, c, b)
        move(1, a, b, c)
        move(n-1, b, a, c)


# A ==> C
# A ==> B
# C ==> B
# A ==> C
# B ==> A
# B ==> C
# A ==> C
move(3, "A", "B", "C")

